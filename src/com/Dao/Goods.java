package com.Dao;

public class Goods {
	private int goodsId;
	private int goodsNumber;
	private float goodsBid;
	private float goodsSell;
	private float goodsSum;
	private float goodsRemain;
	private String goodsName;
	private String goodsAreaNum;
	private String goodsAreaName;
	
	
	
	public String getGoodsAreaNum() {
		return goodsAreaNum;
	}
	public void setGoodsAreaNum(String goodsAreaNum) {
		this.goodsAreaNum = goodsAreaNum;
	}
	public String getGoodsAreaName() {
		return goodsAreaName;
	}
	public void setGoodsAreaName(String goodsAreaName) {
		this.goodsAreaName = goodsAreaName;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public int getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}
	public int getGoodsNumber() {
		return goodsNumber;
	}
	public void setGoodsNumber(int goodsNumber) {
		this.goodsNumber = goodsNumber;
	}
	public float getGoodsBid() {
		return goodsBid;
	}
	public void setGoodsBid(float goodsBid) {
		this.goodsBid = goodsBid;
	}
	public float getGoodsSell() {
		return goodsSell;
	}
	public void setGoodsSell(float goodsSell) {
		this.goodsSell = goodsSell;
	}
	public float getGoodsSum() {
		return goodsSum;
	}
	public void setGoodsSum(float goodsSum) {
		this.goodsSum = goodsSum;
	}
	public float getGoodsRemain() {
		return goodsRemain;
	}
	public void setGoodsRemain(float goodsRemain) {
		this.goodsRemain = goodsRemain;
	}

}
